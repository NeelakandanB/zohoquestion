package com.zoho.zohograduatestudies.assignment4.question7;

public class AdvanceCalculator {
    double pow(int a, int exponential) {
        return Math.pow(a, exponential);
    }

    double mod(int a, int b) {
        return a % b;
    }

    double square(double a) {
        return Math.sqrt(a);
    }

}
