package com.zoho.zohograduatestudies.assignment6.Question2.arithmetic;

public class ArithmeticOperations
{
    public  int addition(int a,int b) // addition
    {
           return a+b;
    }
    public int substraction(int a,int b ) // Substraction
    {
           return a-b;
    }
    public int multiplication(int a,int b) //multiplication
    {
          return a * b;
    }

    public double division(double a,double b) //Division
    {
          return a / b;
    }

    public int  modulo(int a,int b) // modulo
    {
          return a%b;
    }

}
